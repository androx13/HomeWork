# –ó–∞–≤–¥–∞–Ω–Ω—è 3

'''
–ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ —Ä–æ–∑–≤'—è–∑—É—î –∫–≤–∞–¥—Ä–∞—Ç–Ω–µ —Ä—ñ–≤–Ω—è–Ω–Ω—è ùëéùë• 2 + ùëèùë• + ùëê = 0 —É –¥—ñ–π—Å–Ω–∏—Ö —á–∏—Å–ª–∞—Ö. 
–ù–∞ –≤—ñ–¥–º—ñ–Ω—É –≤—ñ–¥ –∞–Ω–∞–ª–æ–≥—ñ—á–Ω–æ—ó –≤–ø—Ä–∞–≤–∏ –∑ –º–∏–Ω—É–ª–æ–≥–æ —É—Ä–æ–∫—É, –ø—Ä–æ–≥—Ä–∞–º–∞ –ø–æ–≤–∏–Ω–Ω–∞ –≤–∏–¥–∞–≤–∞—Ç–∏ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ –≤—ñ–¥—Å—É—Ç–Ω—ñ—Å—Ç—å –¥—ñ–π—Å–Ω–∏—Ö –∫–æ—Ä–µ–Ω—ñ–≤, 
—è–∫—â–æ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–∏—Å–∫—Ä–∏–º—ñ–Ω–∞–Ω—Ç–∞ ùê∑ = ùëè 2 ‚àí4ùëéùëê –Ω–µ–≥–∞—Ç–∏–≤–Ω–µ, —î–¥–∏–Ω–µ —Ä—ñ—à–µ–Ω–Ω—è ùë• = ‚àí ùëè / 2ùëé , —è–∫—â–æ –≤—ñ–Ω –¥–æ—Ä—ñ–≤–Ω—é—î –Ω—É–ª—é, –∞–±–æ –¥–≤–∞ –∫–æ—Ä–µ–Ω—ñ ùë•1,2 = ‚àíb¬±D‚àí‚àí‚àö/2a
, —è–∫—â–æ –≤—ñ–Ω –ø–æ–∑–∏—Ç–∏–≤–Ω–∏–π.
'''

a = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ a: "))
b = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ b: "))
c = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ c: "))

discriminant = pow(b, 2) - 4 * a * c
if discriminant < 0:
        print("–ö–æ—Ä–Ω–µ–π –Ω–µ—Ç")
elif discriminant == 0:
        x = (-b + pow(discriminant, 1 / 2)) / (2 * a)
        print(f'"–ö–æ—Ä–µ–Ω—å –æ–¥–∏–Ω –∏ —Ä–∞–≤–µ–Ω =" {x}')
else:
        x1 = (-b + pow(discriminant, 1 / 2)) / (2 * a)
        x2 = (-b - pow(discriminant, 1 / 2)) / (2 * a)
        print(f'–ü–µ—Ä–≤—ã–π –∫–æ—Ä–µ–Ω—å: {x1}')
        print(f'–í—Ç–æ—Ä–æ–π –∫–æ—Ä–µ–Ω—å: {x2}')


