# –ó–∞–≤–¥–∞–Ω–Ω—è 3
'''
–ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ —Ä–æ–∑–≤'—è–∑—É—î –∫–≤–∞–¥—Ä–∞—Ç–Ω–µ —Ä—ñ–≤–Ω—è–Ω–Ω—è ùëéùë•2 + ùëèùë• + ùëê = 0 –∑–∞ —Ñ–æ—Ä–º—É–ª–∞–º–∏ ùë•1,2 =  ‚àíùëè¬± b2‚àí4ac‚àí‚àí‚àí‚àí‚àí‚àí‚àí‚àö
 / 2ùëé . –ó–Ω–∞—á–µ–Ω–Ω—è a, b —Ç–∞ c –≤–≤–æ–¥—è—Ç—å—Å—è –∑ –∫–ª–∞–≤—ñ–∞—Ç—É—Ä–∏. –î–ª—è –∑–Ω–∞—Ö–æ–¥–∂–µ–Ω–Ω—è –∫–æ—Ä–µ–Ω—è –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π—Ç–µ –æ–ø–µ—Ä–∞—Ç–æ—Ä –∑–≤–µ–¥–µ–Ω–Ω—è –≤ —Å—Ç—É–ø—ñ–Ω—å,
–∞ –Ω–µ —Ñ—É–Ω–∫—Ü—ñ—é math.sqrt, —â–æ–± –æ—Ç—Ä–∏–º–∞—Ç–∏ –∫–æ–º–ø–ª–µ–∫—Å–Ω—ñ —á–∏—Å–ª–∞ —É –≤–∏–ø–∞–¥–∫—É, —è–∫—â–æ –≤–∏—Ä–∞–∑ –ø—ñ–¥ –∫–æ—Ä–µ–Ω–µ–º –Ω–µ–≥–∞—Ç–∏–≤–Ω–∏–π.
'''

a = float(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ a: "))
b = float(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ b: "))
c = float(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ c: "))

discriminant = pow(b, 2) - 4 * a * c
if discriminant < 0:
    x1 = (-b + pow(discriminant, 1 / 2)) / (2 * a)
    x2 = (-b - pow(discriminant, 1 / 2)) / (2 * a)
    print(f'–ü–µ—Ä–≤—ã–π –∫–æ—Ä–µ–Ω—å: {x1} –í—Ç–æ—Ä–æ–π –∫–æ—Ä–µ–Ω—å: {x2}')
elif discriminant == 0:
    x = (-b + pow(discriminant, 1 / 2)) / (2 * a)
    print(f'"–ö–æ—Ä–µ–Ω—å –æ–¥–∏–Ω –∏ —Ä–∞–≤–µ–Ω =" {x}')
else:
    real_part = -b / (2 * a)
    imag_part = (-discriminant) ** 0.5 / (2 * a)
    print("–ö–æ—Ä–Ω–∏ —É—Ä–æ–≤–Ω–µ–Ω–∏—è: ", complex(real_part, imag_part), "–∏", complex(real_part, -imag_part))

# –ó–∞–≤–¥–∞–Ω–Ω—è 4

