# Створіть програму, яка приймає як формальні параметри зріст і вагу користувача,
# обчислює індекс маси тіла і в залежності від результату повертає інформаційне повідомлення
# (маса тіла в нормі, недостатня вага або слідкуйте за фігурою).
# Користувач з клавіатури вводить значення росту та маси тіла та передає ці дані у вигляді
# фактичних параметрів під час виклику функції.
# Програма працює доти, доки користувач не зупинить її комбінацією символів «off».

import math


def calc_bmi(height, weight):
    bmi = weight / pow(height, 2)
    return bmi


def print_bmi(bmi):
    if bmi < 18.5:
        return "Недостаточный вес"
    elif bmi > 18.5 < 25:
        return "Масса тела в норме"
    else:
        return "Избыточный вес"


while True:
    print("Калькулятор индекса массы тела\n"
          "-------------------------------")
    height = float(input("Введите рост в метрах: "))
    weight = float(input("Введите вес в килограммах: "))
    bmi = calc_bmi(height, weight)
    print("Индекс массы тела - {:.2f}. {}\n".format(bmi, print_bmi(bmi)))

    stop = input("Продолжить? Введите \"off\" , чтобы остановить:  ")
    if stop == "off":
        break
