# Створіть програму, яка складається з функції,
# яка приймає три числа і повертає їх середнє арифметичне, і головного циклу,
# що запитує у користувача числа і обчислює їх середні значення за допомогою створеної функції.

def arithmetic_mean(a, b, c):
    return (a + b + c) / 3


while True:
    try:
        num1 = float(input("Введите первое число: "))
        num2 = float(input("Введите второе число: "))
        num3 = float(input("Введите третье число: "))
        print("Среднее арифметическое = ", arithmetic_mean(num1, num2, num3), "\n")
    except ValueError:
        print("Ошибка, введите число!")
    stop = input("Продолжить? Да/Нет: ")
