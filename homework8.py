import math


def add(a, b):
    return a + b


def substract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    if b != 0:
        return a / b
    else:
        print("Ошибка, деление на ноль невозможно")
        return None


def power(a, b):
    return pow(a, b)


def square_root(a):
    if a >= 0:
        return math.sqrt(a)
    else:
        print("Ошибка, у отрицательного числа нет корней")
        return None


def cube_root(a):
    if a >= 0:
        return a ** (1 / 3)
    else:
        print("Ошибка, у отрицательного числа нет корней")
        return None


