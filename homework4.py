# Створіть програму, яка малює на екрані прямокутник із зірочок заданою користувачем ширини та висоти.

a = int(input("Введите ширину: "))
b = int(input("Введите высоту"))
for n in range(a):
    for n in range(b):
        print("*", end="")
    print()

