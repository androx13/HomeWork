# Завдання 1

# Дано числа a і b (a < b). Виведіть суму всіх натуральних чисел від a до b (включно).

a = int(input("Input number a: "))
b = int(input("Input number b: "))
x = 0
if a > b:
    print("Error")
else:
    for n in range(a, b + 1):
        x += n
    print(x)

