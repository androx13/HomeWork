# Факторіалом числа n називається число n!=1∙2∙3∙…∙n. Створіть програму, яка обчислює факторіал введеного користувачем числа.

x = int(input("Input number: "))

for n in range(1, x):
    x *= n
print(x)

# Завдання 3

# Використовуючи вкладені цикли та функції print(‘*’, end=’’), print(‘ ‘, end=’’) та print() виведіть на екран прямокутний трикутник

n = int(input("Введите число рядков: "))
for i in range(n):
    for j in range(i + 1):
        print('*', end="")
    print()

# Завдання 4

# Дано числа a і b (a < b).
# Виведіть на екран суму всіх натуральних чисел від a до b (включно),
# які є кратними середньому арифметичному цього проміжку.

a = int(input("Input a: "))
b = int(input("Input b: "))
mid = (a + b) / 2
total = 0
for n in range(a, b + 1):
    if n % mid == 0:
        total += 1
        print("Сумма равна: ", total)

